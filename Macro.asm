;MACRO========================================================================================================================================================
.MACRO STSI
	LDI R16, @1
	STS @0, R16
.ENDM

.MACRO OUTI
	PUSH r16
	LDI r16, @1
	STS @1, r16
	pop r16
.ENDM
.macro ADIC
	push r16
	LDI r16, @1
	ADC @0, R16
	pop r16
.ENDM
//=================================================================